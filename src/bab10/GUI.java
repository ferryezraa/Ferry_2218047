   public void insert() {
    String merk = txtMerk.getText();
    String model = txtModel.getText();
    String warna = txtWarna.getText();
    String tipe = txtTipe.getText();
    int nikotin = Integer.parseInt(txtNikotin.getText());
    double harga = Double.parseDouble(txtHarga.getText());
    int stok = Integer.parseInt(txtStok.getText());
    try {
        koneksi();
        Statement statement = conn.createStatement();
        statement.executeUpdate("INSERT INTO tb_rokok (Merk,Model,Warna,Tipe,Nikotin,Harga,Stok)"
                + "VALUES('" + merk + "','" + model + "','" + warna + "','" + tipe + "','" + nikotin + "','" + harga + "','" + stok + "')");
        statement.close();
        JOptionPane.showMessageDialog(null, "Berhasil Memasukan Data Rokok!" + "\n");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, "Terjadi Kesalahan saat mengakses Database!", "Error", JOptionPane.ERROR_MESSAGE);
    }
    refresh();
}
    
