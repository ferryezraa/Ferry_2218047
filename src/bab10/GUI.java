    public void tampil() {
        DefaultTableModel tabelhead = new DefaultTableModel();
        tabelhead.addColumn("Merk");
        tabelhead.addColumn("Model");
        tabelhead.addColumn("Warna");
        tabelhead.addColumn("Tipe");
        tabelhead.addColumn("Nikotin");
        tabelhead.addColumn("Harga");
        tabelhead.addColumn("Stok");
        try {
            koneksi();
            String sql = "SELECT * FROM tb_rokok";
            Statement stat = conn.createStatement();
            ResultSet res = stat.executeQuery(sql);
            while (res.next()) {
                tabelhead.addRow(new Object[]{res.getString(2), res.getString(3), res.getString(4), res.getString(5), res.getString(6), res.getString(7),res.getString(8)});
            }
            tabel_rokok.setModel(tabelhead);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "BELUM TERKONEKSI");
        }
    }
    
